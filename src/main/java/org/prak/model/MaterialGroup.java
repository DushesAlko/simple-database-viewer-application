package org.prak.model;

public class MaterialGroup {
    private int id;
    private String name;

    public MaterialGroup(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return name;
    }
}
